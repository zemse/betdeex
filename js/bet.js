function getValue() {
	const value = $("#value").val();
	let team = $("#option").val();
	if(team === "Yes"){
		team = 1 
	}
	else {
		team = 0
	}
	ethereum.enable().then((account) => {
		web3.eth.defaultAccount = account[0];
		console.log(web3.eth.defaultAccount);
		
		BetContract.methods.bet(team).send({from:web3.eth.defaultAccount, value:web3.utils.toWei(value,"ether")});
	
	})
}

